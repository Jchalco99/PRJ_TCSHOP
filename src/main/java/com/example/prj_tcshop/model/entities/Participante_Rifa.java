package com.example.prj_tcshop.model.entities;

public class Participante_Rifa {
    private String id;
    private String numero;
    private String idrifa;
    private String idUsuario;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getIdrifa() {
        return idrifa;
    }

    public void setIdrifa(String idrifa) {
        this.idrifa = idrifa;
    }

    public String getIdUsuario() {
        return idUsuario;
    }

    public void setIdUsuario(String idUsuario) {
        this.idUsuario = idUsuario;
    }
}
